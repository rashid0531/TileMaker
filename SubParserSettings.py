class ArgumentSubParser:
    def __init__(self, name_arg=None, action_arg=None, number_of_args=None, const_arg=None, default_arg=None,
                 type_arg=None, choices_arg=None, required_arg=None, help_arg=None, metavar_arg=None, destination_arg=None):
        self.name_arg = name_arg
        self.action_arg = action_arg
        self.number_of_args = number_of_args
        self.const_arg = const_arg
        self.default_arg = default_arg
        self.type_arg = type_arg
        self.choices_arg = choices_arg
        self.required_arg = required_arg
        self.help_arg = help_arg
        self.metavar_arg = metavar_arg
        self.destination_arg = destination_arg



